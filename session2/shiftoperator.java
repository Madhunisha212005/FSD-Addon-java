package programs;

public class shiftoperator {
	
	 public static void main(String[] args) {
		
		 //right shift
		 System.out.println(10>>5);
		 System.out.println(10>>2);
		 System.out.println(20>>4);
		 
		 //left shift
		 System.out.println(10<<5);
		 System.out.println(10<<2);
		 System.out.println(20<<4);
	}

}
